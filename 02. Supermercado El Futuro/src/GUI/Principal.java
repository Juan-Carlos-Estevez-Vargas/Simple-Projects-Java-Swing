    public Principal() {
        initComponents();
        this.setTitle("Supermercado El Furuto");
        this.setResizable(false);
        this.setLocationRelativeTo(null);

        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "SELECT id_producto, nombre_producto, cantidad_producto, precio_producto, categoria FROM productos");

            ResultSet rs = pst.executeQuery();

            tabla_productos = new JTable(model);
            jScrollPane1.setViewportView(tabla_productos);

            //Columnas de la tabla
            model.addColumn("ID");
            model.addColumn("Nombre");
            model.addColumn("Cantidad");
            model.addColumn("Precio");
            model.addColumn("Categoría");

            //Llenado de la tabla
            while (rs.next()) {
                Object[] fila = new Object[5];

                for (int i = 0; i < 5; i++) {
                    fila[i] = rs.getObject(i + 1);
                }

                model.addRow(fila);
            }
            cn.close();

        } catch (SQLException e) {
            System.err.println("Error en el llenado de la tabla clientes " + e);
        }

    }

