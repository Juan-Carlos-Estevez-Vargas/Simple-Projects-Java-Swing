    private void btnGuardarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarProductoActionPerformed
        // Creación de variables
        String nombre = txtNombreProducto.getText().trim();
        int cantidad = Integer.parseInt(txtCantidadProducto.getText().trim());
        double precio = Double.parseDouble(txtPrecioProducto.getText().trim());
        String categoria = cmbCategoria.getSelectedItem().toString();

        //Volvemos a realizar la conexion a la base de datos
        try {
            try ( Connection cn2 = Conexion.conectar()) {
                PreparedStatement pst2 = cn2.prepareStatement(
                        "INSERT INTO productos VALUES (?,?,?,?,?)");

                //Seteamos los valores de los txt a la base de datos (Porque es una sentencia INSERT)
                pst2.setInt(1, 0); //Se insertara el id, y como es numerico e incrementable le enviamos un 0
                pst2.setString(2, nombre);
                pst2.setInt(3, cantidad);
                pst2.setDouble(4, precio);
                pst2.setString(5, categoria);

                pst2.executeUpdate(); //Ejecutamos los cambios (La sentencia SQL)

                cn2.close();
            }

            //Ahora procedemos a limpiar los campos de texto
            Limpiar();

            //Iluminamos los campos de texto
            txtCantidadProducto.setBackground(Color.green);
            txtNombreProducto.setBackground(Color.green);
            txtPrecioProducto.setBackground(Color.green);

            JOptionPane.showMessageDialog(null, "Registro exitoso");

        } catch (SQLException e) {
            System.err.println("Error en registrar producto " + e);
            JOptionPane.showMessageDialog(null, "¡¡Error al registrar producto!! Contacte al Administrador");
        }
    }//GEN-LAST:event_btnGuardarProductoActionPerformed

